    r'''Makes string tuplet ratios.
    '''
    if n == 1:
        return [
            (1,), (1,), (1,), (1,), (1,), (1, 4),
            (1,), (1,), (1,), (1,), (1,), (2, 3),
            (1,), (1,), (1,), (1,), (1,), (3, 2),
            (1,), (1,), (1,), (1,), (1,), (4, 1),
            (1,), (1,), (1,), (1,), (1,), (1, 4),
            ]
    elif n == 2:
        return [
            (1,), (1,), (1,), (1,), (1, 4),
            (1,), (1,), (1,), (1,), (2, 3),
            (1,), (1,), (1,), (1,), (3, 2),
            (1,), (1,), (1,), (1,), (4, 1),
            (1,), (1,), (1,), (1,), (1, 4),
            ]
    elif n == 3:
        return [
            (1,), (1,), (1,), (1, 4),
            (1,), (1,), (1,), (2, 3),
            (1,), (1,), (1,), (3, 2),
            (1,), (1,), (1,), (4, 1),
            (1,), (1,), (1,), (1, 4),
            ]
    elif n == 4:
        return [
            (1,), (1,), (1, 4),
            (1,), (1,), (2, 3),
            (1,), (1,), (3, 2),
            (1,), (1,), (4, 1),
            (1,), (1,), (1, 4),
            ]
    else:
        raise ValueError(n)
